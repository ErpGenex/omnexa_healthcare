# Keep codes / single letters as-is
_PRESERVE = re.compile(
	r"^([A-Z]{1,3}[\+\-]?|\d+|[IVXLC]+|iOS|Android|CT|MR|MG|NM|XR|US|LWBS|UNK|HACC)$"
)

_SEGMENT_SPLIT = re.compile(r"\s*[/·&]\s*|\s+/\s+")


def _preserve(text: str) -> bool:
	return bool(_PRESERVE.match(text.strip()))


def _translate_tokens(text: str) -> str:
	parts = re.split(r"(\s+|[-/·&()])", text)
	out: list[str] = []
	for part in parts:
		if not part or part.isspace() or part in "-/·&()":
			out.append(part)
			continue
		key = part.strip()
		if _preserve(key):
			out.append(key)
			continue
		if key in TOKEN_AR:
			out.append(TOKEN_AR[key])
		elif key.title() in TOKEN_AR:
			out.append(TOKEN_AR[key.title()])
		else:
			out.append(part)
	return "".join(out)


def translate_to_ar(text: str) -> str:
	"""Best-effort Arabic label for a healthcare UI string."""
	if not text:
		return text
	raw = text.strip()
	if _preserve(raw):
		return raw
	if raw in PATIENT_TERMINOLOGY_AR:
		return PATIENT_TERMINOLOGY_AR[raw]
	for bucket in (WORKSPACE_AR, DOCTYPE_AR, REPORT_AR, ENTITY_AR, FIELD_AR, BULK_FIELD_AR, PHRASE_FIX_AR):
		if raw in bucket:
			return bucket[raw]
	if raw.startswith("Healthcare "):
		inner = raw[11:]
		if inner in DOCTYPE_AR:
			return DOCTYPE_AR[raw]
		if inner in ENTITY_AR:
			return ENTITY_AR[inner]
		translated_inner = translate_to_ar(inner)
		if translated_inner != inner:
			return translated_inner
	# snake_case / technical values
	if raw.islower() and ("_" in raw or "-" in raw or raw in {"draft", "active", "inactive"}):
		if raw in BULK_FIELD_AR:
			return BULK_FIELD_AR[raw]
		if raw in PHRASE_FIX_AR:
			return PHRASE_FIX_AR[raw]
		human = raw.replace("_", " ").replace("-", " ")
		if human != raw:
			return translate_to_ar(human.title())
	# Multi-segment labels
	if _SEGMENT_SPLIT.search(raw):
		segments = _SEGMENT_SPLIT.split(raw)
		sep = " · " if "·" in raw else " / " if "/" in raw else " & "
		return sep.join(translate_to_ar(s.strip()) for s in segments if s.strip())
	# Parenthetical FHIR etc.
	if "(" in raw and raw not in FIELD_AR:
		main, _, rest = raw.partition("(")
		inner = rest.rstrip(")")
		return f"{translate_to_ar(main.strip())} ({translate_to_ar(inner.strip())})"
	tokenized = _translate_tokens(raw)
	if tokenized != raw and not _has_untranslated_english(tokenized):
		return tokenized
	return raw


_EN_ALLOW = re.compile(
	r"\b(?:FHIR|ICD|SNOMED|DRG|LOINC|HL7|EDI|X12|NPHIES|DICOM|JSON|ISO|URI|URL|WADO|PACS|UCUM|GS1|UDI|MRN|ESI|ASA|CVX|MAR|ADT|LIS|EMR|MPI|PHI|CDS|IPS|ERP|FEFO|ABO|Rh|SKU|Android|iOS|SMS|SSN|AMA|ASTM|LWBS|UNK|HACC|eMAR|OT|OPD|ICU|IPD|HDU|DMY|RF|PT|NM|MG|XR|US|CT|MR|WHO)\b"
)


def _has_untranslated_english(text: str) -> bool:
	cleaned = _EN_ALLOW.sub("", text)
	return bool(re.search(r"[A-Za-z]{3,}", cleaned))


def normalize_en(text: str) -> str:
	"""Patient-first English normalization for en.csv overrides."""
	if not text:
		return text
	return PATIENT_TERMINOLOGY_EN.get(text, text)
